async def _handle_video_compression_mode(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None:
    uid = update.effective_user.id
    msg: Message = update.message

    is_video_document = bool(msg.document and msg.document.mime_type and msg.document.mime_type.startswith("video/"))

    if msg.video:
        file_id = msg.video.file_id
        mime_type = msg.video.mime_type or "video/mp4"
        file_size = msg.video.file_size
        output_name = "compressed_video.mp4"
    elif is_video_document:
        file_id = msg.document.file_id
        mime_type = msg.document.mime_type or "video/mp4"
        file_size = msg.document.file_size
        base_name = msg.document.file_name or "video"
        output_name = os.path.splitext(base_name)[0] + "_compressed.mp4"
    else:
        await msg.reply_text(
            "В этом режиме нужно отправить именно видео. Текст, фото и другие файлы игнорируются.",
            reply_markup=InlineKeyboardMarkup([[InlineKeyboardButton("Отмена", callback_data=CB_BACK)]]),
        )
        return

    if file_size and file_size > TELEGRAM_MAX_DOWNLOAD_BYTES:
        await msg.reply_text(
            f"Файл слишком большой для скачивания через обычный Telegram Bot API: {_bytes_to_mb(file_size)} МБ. "
            f"Текущий лимит: {TELEGRAM_MAX_DOWNLOAD_MB} МБ. Для больших файлов нужен локальный Telegram Bot API Server или предварительное сжатие до отправки.",
            reply_markup=kb_main(),
        )
        set_state(uid, "idle")
        return

    set_state(uid, "processing")
    await msg.reply_text("Сжимаю видео...")
    await context.bot.send_chat_action(chat_id=msg.chat_id, action="upload_document")

    cleanup_paths: list[str] = []
    try:
        tg_file = await context.bot.get_file(file_id)
        video_bytes = await tg_file.download_as_bytearray(read_timeout=120, write_timeout=120)
        final_path, report, cleanup_paths = await compress_video_for_user(bytes(video_bytes), mime_type)
        output_size = os.path.getsize(final_path) if os.path.exists(final_path) else 0
        caption = f"Готово. {report}\nИтоговый размер: {_bytes_to_mb(output_size)} МБ"
        with open(final_path, "rb") as f:
            await msg.reply_document(
                document=f,
                filename=output_name,
                caption=caption[:1024],
                read_timeout=120,
                write_timeout=120,
            )
        await msg.reply_text(
            "Можешь отправить следующее видео для сжатия или нажать «Отмена».",
            reply_markup=InlineKeyboardMarkup([[InlineKeyboardButton("Отмена", callback_data=CB_BACK)]]),
        )
        set_state(uid, "compress_video")
    except Exception as e:
        logger.error("User video compression error: %s", e, exc_info=True)
        set_state(uid, "compress_video")
        await msg.reply_text(
            f"Ошибка при сжатии видео: {e}",
            reply_markup=InlineKeyboardMarkup([[InlineKeyboardButton("Отмена", callback_data=CB_BACK)]]),
        )
    finally:
        for path in cleanup_paths:
            try:
                if path and os.path.exists(path):
                    os.remove(path)
            except Exception:
                pass


async def handle_message(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None:
    uid = update.effective_user.id
    msg: Message = update.message
    state = get_state(uid)

    if state == "processing":
        await msg.reply_text("Идёт обработка, подожди немного...")
        return

    if state == "compress_video":
        await _handle_video_compression_mode(update, context)
        return

    if state != "loading":
        await msg.reply_text(
            "Нажми «Загрузить ТЗ» чтобы начать.",
            reply_markup=kb_main(),
        )
        return

    text_buf = user_text_buffers.setdefault(uid, [])
    media_buf = user_media_buffers.setdefault(uid, [])
    video_buf = user_video_buffers.setdefault(uid, [])
    image_buf = user_image_buffers.setdefault(uid, [])

    is_image_document = bool(
        msg.document
        and msg.document.mime_type
        and msg.document.mime_type.startswith("image/")
    )
    is_video_document = bool(
        msg.document
        and msg.document.mime_type
        and msg.document.mime_type.startswith("video/")
    )
    is_media = bool(
        msg.audio or msg.voice or msg.sticker or msg.animation or
        (msg.document and not is_video_document and not is_image_document)
    )
    extracted_text = extract_text_with_telegram_entities(msg)
    if extracted_text:
        text_buf.append(extracted_text)

    if msg.video:
        video_buf.append((
            msg.video.file_id,
            msg.video.mime_type or "video/mp4",
            msg.video.file_size,
        ))
    elif is_video_document:
        video_buf.append((
            msg.document.file_id,
            msg.document.mime_type or "video/mp4",
            msg.document.file_size,
        ))
    elif msg.photo:
        photo = msg.photo[-1]
        image_buf.append((photo.file_id, "image/jpeg", photo.file_size, "telegram_photo"))
        media_buf.append((msg.chat_id, msg.message_id))
    elif is_image_document:
        image_buf.append((
            msg.document.file_id,
            msg.document.mime_type or "image/jpeg",
            msg.document.file_size,
            msg.document.file_name or "image_document",
        ))
        media_buf.append((msg.chat_id, msg.message_id))
    elif is_media:
        media_buf.append((msg.chat_id, msg.message_id))

    parts = []
    if text_buf:
        parts.append(f"текст: {count_label(len(text_buf))}")
    if video_buf:
        parts.append(f"видео: {len(video_buf)} шт.")
    if image_buf:
        parts.append(f"изображения: {len(image_buf)} шт.")
    if media_buf:
        parts.append(f"файлов/фото: {len(media_buf)} шт.")
    status = "Принято. " + ", ".join(parts) + "."

    await msg.reply_text(status, reply_markup=kb_loading(uid))


