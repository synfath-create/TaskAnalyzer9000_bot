def _bytes_to_mb(size_bytes: int | None) -> float:
    if not size_bytes:
        return 0.0
    return round(size_bytes / 1024 / 1024, 2)


def _ffmpeg_available() -> bool:
    return shutil.which("ffmpeg") is not None


def _guess_video_suffix(mime_type: str) -> str:
    low = (mime_type or "").lower()
    if "quicktime" in low or "mov" in low:
        return ".mov"
    if "webm" in low:
        return ".webm"
    if "x-matroska" in low or "mkv" in low:
        return ".mkv"
    return ".mp4"


def _compression_profile(size_bytes: int) -> dict[str, str]:
    """Подбирает профиль сжатия под размер файла. Приоритет — читаемость UI/субтитров."""
    mb = _bytes_to_mb(size_bytes)
    if mb <= 80:
        return {"scale": "min(1280,iw)", "fps": "24", "crf": "28", "audio": "96k", "label": "720p/24fps/CRF28"}
    if mb <= 200:
        return {"scale": "min(1280,iw)", "fps": "20", "crf": "30", "audio": "80k", "label": "720p/20fps/CRF30"}
    return {"scale": "min(854,iw)", "fps": "20", "crf": "32", "audio": "64k", "label": "480p/20fps/CRF32"}


def _compress_video_file(input_path: str, output_path: str, input_size_bytes: int) -> tuple[bool, str]:
    """Сжимает видео через FFmpeg. Возвращает success + технический отчёт."""
    if not VIDEO_COMPRESSION_ENABLED:
        return False, "сжатие отключено через VIDEO_COMPRESSION_ENABLED=0"
    if not _ffmpeg_available():
        return False, "FFmpeg не найден в окружении"

    profile = _compression_profile(input_size_bytes)
    vf = f"scale='{profile['scale']}':-2,fps={profile['fps']}"
    cmd = [
        "ffmpeg", "-y",
        "-i", input_path,
        "-vf", vf,
        "-c:v", "libx264",
        "-preset", "veryfast",
        "-crf", profile["crf"],
        "-c:a", "aac",
        "-b:a", profile["audio"],
        "-movflags", "+faststart",
        output_path,
    ]

    logger.info("Video compression started: profile=%s input_mb=%s", profile["label"], _bytes_to_mb(input_size_bytes))
    completed = subprocess.run(
        cmd,
        stdout=subprocess.PIPE,
        stderr=subprocess.PIPE,
        text=True,
        timeout=VIDEO_COMPRESS_TIMEOUT_SEC,
    )
    if completed.returncode != 0:
        logger.warning("FFmpeg compression failed: %s", completed.stderr[-1500:])
        return False, f"FFmpeg error: {completed.stderr[-500:]}"

    if not os.path.exists(output_path) or os.path.getsize(output_path) <= 0:
        return False, "FFmpeg не создал итоговый файл"

    output_size = os.path.getsize(output_path)
    logger.info(
        "Video compression finished: input_mb=%s output_mb=%s profile=%s",
        _bytes_to_mb(input_size_bytes), _bytes_to_mb(output_size), profile["label"],
    )
    return True, f"сжато: {_bytes_to_mb(input_size_bytes)} МБ → {_bytes_to_mb(output_size)} МБ, профиль {profile['label']}"


def _prepare_video_file_for_gemini(input_path: str, mime_type: str) -> tuple[str, str, str, list[str]]:
    """
    Готовит видео к Gemini: при необходимости сжимает и возвращает путь файла для upload.
    Возвращает: final_path, final_mime_type, report, paths_to_cleanup.
    """
    cleanup_paths: list[str] = []
    input_size = os.path.getsize(input_path)
    input_mb = _bytes_to_mb(input_size)

    if not VIDEO_COMPRESSION_ENABLED:
        return input_path, mime_type, f"сжатие отключено; размер {input_mb} МБ", cleanup_paths

    if input_size < VIDEO_COMPRESS_THRESHOLD_BYTES:
        return input_path, mime_type, f"сжатие не требуется; размер {input_mb} МБ меньше порога {VIDEO_COMPRESS_THRESHOLD_MB} МБ", cleanup_paths

    compressed_path = tempfile.NamedTemporaryFile(suffix="_compressed.mp4", delete=False).name
    cleanup_paths.append(compressed_path)
    ok, report = _compress_video_file(input_path, compressed_path, input_size)
    if not ok:
        return input_path, mime_type, f"сжатие не выполнено: {report}; отправлен оригинал {input_mb} МБ", cleanup_paths

    compressed_size = os.path.getsize(compressed_path)
    if compressed_size >= input_size:
        return input_path, mime_type, f"сжатие не уменьшило файл; отправлен оригинал {input_mb} МБ", cleanup_paths

    if compressed_size > VIDEO_TARGET_MAX_BYTES:
        report += f"; предупреждение: итоговый файл больше целевого лимита {VIDEO_TARGET_MAX_MB} МБ"

    return compressed_path, "video/mp4", report, cleanup_paths


def _prepare_video_file_for_user_compression(input_path: str, mime_type: str) -> tuple[str, str, str, list[str]]:
    """Отдельный режим: сжимает видео по запросу и возвращает файл для отправки обратно."""
    cleanup_paths: list[str] = []
    input_size = os.path.getsize(input_path)
    input_mb = _bytes_to_mb(input_size)

    if not VIDEO_COMPRESSION_ENABLED:
        return input_path, mime_type, f"сжатие отключено; размер {input_mb} МБ", cleanup_paths
    if not _ffmpeg_available():
        return input_path, mime_type, f"FFmpeg не найден; отправлен оригинал {input_mb} МБ", cleanup_paths

    compressed_path = tempfile.NamedTemporaryFile(suffix="_compressed.mp4", delete=False).name
    cleanup_paths.append(compressed_path)
    ok, report = _compress_video_file(input_path, compressed_path, input_size)
    if not ok:
        return input_path, mime_type, f"сжатие не выполнено: {report}; отправлен оригинал {input_mb} МБ", cleanup_paths

    compressed_size = os.path.getsize(compressed_path)
    if compressed_size <= 0:
        return input_path, mime_type, f"сжатие не создало итоговый файл; отправлен оригинал {input_mb} МБ", cleanup_paths

    if compressed_size >= input_size:
        report += "; предупреждение: итоговый файл не меньше оригинала, но отправляется MP4-версия"

    return compressed_path, "video/mp4", report, cleanup_paths


async def compress_video_for_user(video_bytes: bytes, mime_type: str) -> tuple[str, str, list[str]]:
    suffix = _guess_video_suffix(mime_type)
    original_path = tempfile.NamedTemporaryFile(suffix=suffix, delete=False).name
    cleanup_paths = [original_path]
    with open(original_path, "wb") as f:
        f.write(video_bytes)

    final_path, final_mime, report, extra_cleanup = await asyncio.to_thread(
        _prepare_video_file_for_user_compression, original_path, mime_type
    )
    cleanup_paths.extend(extra_cleanup)
    return final_path, report, cleanup_paths


