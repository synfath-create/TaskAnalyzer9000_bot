async def _run_analysis(update: Update, context: ContextTypes.DEFAULT_TYPE, mode: str) -> None:
    query = update.callback_query
    await query.answer()
    uid = query.from_user.id

    text_buf = user_text_buffers.get(uid, [])
    media_buf = user_media_buffers.get(uid, [])
    image_buf = user_image_buffers.get(uid, [])
    video_buf = user_video_buffers.get(uid, [])

    if not text_buf and not media_buf and not image_buf and not video_buf:
        await query.message.reply_text(
            "Буфер пустой — нечего анализировать.",
            reply_markup=kb_loading(uid),
        )
        return

    if mode in {"video_only", "video_text"} and not video_buf:
        await query.message.reply_text(
            "В буфере нет видео. Загрузи видео или выбери «Анализировать ТЗ».",
            reply_markup=kb_loading(uid),
        )
        return

    if mode in {"images_only", "images_text"} and not image_buf:
        await query.message.reply_text(
            "В буфере нет изображений. Загрузи фото или скриншоты.",
            reply_markup=kb_loading(uid),
        )
        return

    if mode == "text_only" and not text_buf:
        await query.message.reply_text(
            "В буфере нет текста ТЗ. Загрузи текст или выбери «Анализировать видео».",
            reply_markup=kb_loading(uid),
        )
        return

    if mode in {"video_text", "images_text"} and not text_buf:
        await query.message.reply_text(
            "Для комбинированного режима нужен текст ТЗ. Загрузи текст или выбери режим анализа только видео/изображений.",
            reply_markup=kb_loading(uid),
        )
        return

    set_state(uid, "processing")
    media_snap = list(media_buf)
    image_snap = list(image_buf)
    video_snap = list(video_buf)
    full_text = "\n\n---\n\n".join(text_buf).strip()

    await context.bot.send_chat_action(chat_id=query.message.chat_id, action="typing")

    video_analysis = None
    if mode in {"video_only", "video_text"} and video_snap:
        count = len(video_snap)
        await query.message.reply_text(f"Анализирую видео через Gemini: {count} шт.")
        analyses = []
        for idx, (file_id, mime_type, file_size) in enumerate(video_snap, 1):
            try:
                if file_size and file_size > TELEGRAM_MAX_DOWNLOAD_BYTES:
                    analyses.append(
                        f"Видео {idx}: не удалось обработать — файл больше лимита скачивания {TELEGRAM_MAX_DOWNLOAD_MB} МБ. "
                        "Автосжатие работает после скачивания файла, поэтому для таких файлов нужен локальный Telegram Bot API Server или предварительное сжатие перед отправкой."
                    )
                    continue

                await context.bot.send_chat_action(chat_id=query.message.chat_id, action="typing")
                tg_file = await context.bot.get_file(file_id)
                video_bytes = await tg_file.download_as_bytearray(read_timeout=120, write_timeout=120)
                result = await analyze_video_gemini(bytes(video_bytes), mime_type)
                label = f"Видео {idx}:\n" if count > 1 else ""
                analyses.append(f"{label}{result}".strip())
            except Exception as e:
                logger.error("Video error: %s", e, exc_info=True)
                analyses.append(f"Видео {idx}: не удалось обработать — {e}")

        video_analysis = "\n\n".join(analyses)

        if "503 UNAVAILABLE" in video_analysis or "high demand" in video_analysis:
            set_state(uid, "loading")
            user_text_buffers[uid] = text_buf
            user_media_buffers[uid] = media_snap
            user_video_buffers[uid] = video_snap
            user_image_buffers[uid] = image_snap
            await query.message.reply_text(
                "Gemini сейчас перегружен и не смог проанализировать видео. "
                "Файл в буфере сохранён — попробуй повторить анализ позже.",
                reply_markup=kb_loading(uid),
            )
            return

    image_analysis = None
    if mode in {"images_only", "images_text"} and image_snap:
        count = len(image_snap)
        await query.message.reply_text(f"Анализирую изображения через OpenAI Vision: {count} шт.")
        images_for_ai = []
        for idx, (file_id, mime_type, file_size, label) in enumerate(image_snap, 1):
            try:
                if file_size and file_size > TELEGRAM_MAX_IMAGE_DOWNLOAD_BYTES:
                    images_for_ai.append((b"", "image/jpeg", f"Изображение {idx}: файл больше {TELEGRAM_MAX_IMAGE_DOWNLOAD_MB} МБ, пропущено"))
                    continue
                await context.bot.send_chat_action(chat_id=query.message.chat_id, action="typing")
                tg_file = await context.bot.get_file(file_id)
                image_bytes = await tg_file.download_as_bytearray(read_timeout=120, write_timeout=120)
                if not mime_type:
                    mime_type = _guess_image_mime(getattr(tg_file, "file_path", ""))
                images_for_ai.append((bytes(image_bytes), mime_type, label or f"image_{idx}"))
            except Exception as e:
                logger.error("Image download error: %s", e, exc_info=True)

        valid_images = [img for img in images_for_ai if img[0]]
        if valid_images:
            try:
                prompt = GPT_IMAGE_ANALYSIS_PROMPT
                image_analysis = await analyze_images_openai(valid_images, prompt)
            except Exception as e:
                logger.error("OpenAI image analysis error: %s", e, exc_info=True)
                image_analysis = f"Изображения не удалось проанализировать: {e}"
        else:
            image_analysis = "Изображения не удалось проанализировать: нет доступных файлов подходящего размера."

    if mode == "video_text":
        await query.message.reply_text("Собираю подробный комбинированный анализ: ТЗ + видео...")
        gpt_text = full_text
        system_prompt = GPT_STRUCTURE_PROMPT
    elif mode == "video_only":
        await query.message.reply_text("Собираю подробный анализ только видео...")
        gpt_text = "Текстовое ТЗ не предоставлено/игнорируется по выбранному режиму. Проанализируй только видео и переведи его в подробный структурированный production-анализ."
        system_prompt = GPT_STRUCTURE_PROMPT
    elif mode == "images_text":
        if len(image_snap) > 10:
            await query.message.reply_text(
                "Можно анализировать до 10 изображений за раз. Сейчас в буфере больше 10 — очисти буфер или отправь часть изображений.",
                reply_markup=kb_loading(uid),
            )
            set_state(uid, "loading")
            return
        await query.message.reply_text("Собираю подробный комбинированный анализ: ТЗ + изображения...")
        gpt_text = full_text
        video_analysis = None
        system_prompt = GPT_STRUCTURE_PROMPT
    elif mode == "images_only":
        if len(image_snap) > 10:
            await query.message.reply_text(
                "Можно анализировать до 10 изображений за раз. Сейчас в буфере больше 10 — очисти буфер или отправь часть изображений.",
                reply_markup=kb_loading(uid),
            )
            set_state(uid, "loading")
            return
        await query.message.reply_text("Собираю подробный анализ только изображений...")
        gpt_text = "Текстовое ТЗ не предоставлено/игнорируется по выбранному режиму. Проанализируй только изображения: каждое отдельно, затем общий production-вывод."
        video_analysis = None
        system_prompt = GPT_STRUCTURE_PROMPT
    else:
        await query.message.reply_text("Анализирую только текстовое ТЗ...")
        gpt_text = full_text
        video_analysis = None
        image_analysis = None
        system_prompt = GPT_TZ_ONLY_PROMPT

    await context.bot.send_chat_action(chat_id=query.message.chat_id, action="typing")
    try:
        reply = await structure_with_gpt(gpt_text, video_analysis, system_prompt, image_analysis)
    except Exception as e:
        logger.error("GPT error: %s", e, exc_info=True)
        set_state(uid, "idle")
        await query.message.reply_text(
            f"Ошибка при обращении к GPT: {e}",
            reply_markup=kb_main(),
        )
        return

    set_state(uid, "idle")
    user_last_analysis[uid] = reply
    clear_buffers(uid)

    for chunk in split_message(reply):
        try:
            await query.message.reply_text(chunk)
        except Exception as send_err:
            logger.error("Send error: %s", send_err, exc_info=True)
            await query.message.reply_text(
                chunk.encode("utf-8", errors="ignore").decode("utf-8")
            )

    if media_snap and mode not in {"video_only"}:
        await query.message.reply_text("Материалы от заказчика:")
        for chat_id, msg_id in media_snap:
            try:
                await context.bot.forward_message(
                    chat_id=query.message.chat_id,
                    from_chat_id=chat_id,
                    message_id=msg_id,
                )
            except Exception as e:
                logger.warning("Не удалось переслать %s: %s", msg_id, e)

    await query.message.reply_text("Готово.", reply_markup=kb_after_result())


