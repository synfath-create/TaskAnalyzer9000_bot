def _guess_image_mime(file_name: str, fallback: str = "image/jpeg") -> str:
    low = (file_name or "").lower()
    if low.endswith(".png"):
        return "image/png"
    if low.endswith(".webp"):
        return "image/webp"
    if low.endswith(".jpg") or low.endswith(".jpeg"):
        return "image/jpeg"
    return fallback


def _analyze_images_openai_sync(images: list[tuple[bytes, str, str]], prompt: str = GPT_IMAGE_ANALYSIS_PROMPT) -> str:
    content = [{"type": "text", "text": prompt}]
    for idx, (image_bytes, mime_type, label) in enumerate(images, 1):
        b64 = base64.b64encode(image_bytes).decode("utf-8")
        content.append({"type": "text", "text": f"Изображение {idx}: {label}"})
        content.append({
            "type": "image_url",
            "image_url": {"url": f"data:{mime_type};base64,{b64}", "detail": "high"},
        })

    response = openai_client.chat.completions.create(
        model=OPENAI_IMAGE_MODEL,
        messages=[{"role": "user", "content": content}],
        temperature=0.2,
    )
    return response.choices[0].message.content or "OpenAI Vision вернул пустой ответ."


